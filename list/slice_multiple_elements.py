# Slicing elements in an array means accessing a specific range of elements within the array
# The start index is included but the ending index is excluded
print("programming" [3:6])

muscles = ["Biceps", "Triceps", "Deltoid", "Sartorius"]
print(muscles[1:3])
print(muscles[1:2])

print(muscles[0:2])
print(muscles[:2])
print(muscles[2:100])
print(muscles[2:])

print(muscles[-4:-2])
print(muscles[-3:])
print(muscles[:-1])
print(muscles[1:-1])

print(muscles[::2])
print(muscles[::-2])
print(muscles[::-1])


